		<h1> Analytics </h1>
		<p>Analytics uses data and math to answer business questions, discover relationships, predict unknown outcomes and automate decisions. <p/>
		<h1> Artificial Intelligence </h1>
		<p> What is AI-Artificial Intelligence? The term "Artificial Intelligence" refers to the simulation of human intelligence processes by machines, especially computer systems. It also includes Expert systems, voice recognition, machine vision, and natural language processing (NLP).<p/>
		<h1> AR / VR </h1>
		<p> Augmented reality (AR) adds digital elements to a live view often by using the camera on a smartphone. Examples of augmented reality experiences include Snapchat lenses and the game Pokemon Go. Virtual reality (VR) implies a complete immersion experience that shuts out the physical world.<p/>
		<h1> Bioinformatics </h1>
		<p> Bioinformatics is defined as the application of tools of computation and analysis to the capture and interpretation of biological data. It is an interdisciplinary field, which harnesses computer science, mathematics, physics, and biology<p/>
		<h1> Cloud Computing </h1>
		<p> Simply put, cloud computing is the delivery of computing services—including servers, storage, databases, networking, software, analytics, and intelligence—over the Internet (“the cloud”) to offer faster innovation, flexible resources, and economies of scale.<p/>
		<h1> Computational Biology </h1>
		<p> Computational biology refers to the use of data analysis, mathematical modeling and computational simulations to understand biological systems and relationships.<p/>
		<h1> Computer Engineering </h1>
		<p> computer engineers build computers such as PCs, workstations, and supercomputers. They also build computer-based systems such as those found in cars, planes, appliances, electronics, phones, communication networks, and many, many other products.<p/>
		<h1> Computer Forensics </h1>
		<p> Computer forensics is a field of technology that uses investigative techniques to identify and store evidence from a computer device. Often, computer forensics is used to uncover evidence that could be used in a court of law. Computer forensics also encompasses areas outside of investigations.<p/>
		<h1> Computer Graphics </h1>
		<p> Computer Graphics is the creation of pictures with the help of a computer. The end product of the computer graphics is a picture it may be a business graph, drawing, and engineering. In computer graphics, two or three-dimensional pictures can be created that are used for research.<p/>
		<h1> Computer Networks </h1>
		<p> Computer networking refers to interconnected computing devices that can exchange data and share resources with each other. These networked devices use a system of rules, called communications protocols, to transmit information over physical or wireless technologies.<p/>
		<h1> Computer Security </h1>
		<p> computer security, also called cybersecurity, the protection of computer systems and information from harm, theft, and unauthorized use. Computer hardware is typically protected by the same means used to protect other valuable or sensitive equipment—namely, serial numbers, doors and locks, and alarms.<p/>
		<h1> Computer Systems </h1>
		<p> A computer system consists of hardware components that have been carefully chosen so that they work well together and software components or programs that run in the computer. The main software component is itself an operating system that manages and provides services to other programs that can be run in the computer.<p/>
		<h1> Computer Theory </h1>
		<p> In theoretical computer science and mathematics, the theory of computation is the branch that deals with what problems can be solved on a model of computation, using an algorithm, how efficiently they can be solved or to what degree<p/>
		<h1> Cryptography </h1>
		<p> Cryptography is a method of protecting information and communications through the use of codes, so that only those for whom the information is intended can read and process it.<p/>
		<h1> Cybersecurity </h1>
		<p> Cybersecurity is the practice of protecting systems, networks, and programs from digital attacks. These cyberattacks are usually aimed at accessing, changing, or destroying sensitive information; extorting money from users; or interrupting normal business processes.<p/>
		<h1> Database Management </h1>
		<p> Database Management Systems (DBMS) are software systems used to store, retrieve, and run queries on data. A DBMS serves as an interface between an end-user and a database, allowing users to create, read, update, and delete data in the database.<p/>
		<h1> Data Mining </h1>
		<p> Data mining is the process of sorting through large data sets to identify patterns and relationships that can help solve business problems through data analysis. Data mining techniques and tools enable enterprises to predict future trends and make more-informed business decisions.<p/>
		<h1> Data Science </h1>
		<p> Data science is the field of applying advanced analytics techniques and scientific principles to extract valuable information from data for business decision-making, strategic planning and other uses.<p/>
		<h1> Deep Learning </h1>
		<p> Deep learning is a type of machine learning and artificial intelligence (AI) that imitates the way humans gain certain types of knowledge. Deep learning is an important element of data science, which includes statistics and predictive modeling.<p/>

		
	
		<h1> DevOps </h1>
		<p>DevOps is the combination of cultural philosophies, practices, and tools that increases an organization's ability to deliver applications and services at high velocity: evolving and improving products at a faster pace than organizations using traditional software development and infrastructure management processes. <p/>
		<h1> Game Design </h1>
		<p> Game design is the art of applying design and aesthetics to create a game for entertainment or for educational, exercise, or experimental purposes. Increasingly, elements and principles of game design are also applied to other interactions, in the form of gamification.<p/>
		<h1> Human Computer Interaction </h1>
		<p> Human-computer interaction (HCI) is a multidisciplinary field of study focusing on the design of computer technology and, in particular, the interaction between humans (the users) and computers. While initially concerned with computers, HCI has since expanded to cover almost all forms of information technology design.<p/>
		<h1> Information Security </h1>
		<p> nformation security, often referred to as InfoSec, refers to the processes and tools designed and deployed to protect sensitive business information from modification, disruption, destruction, and inspection.<p/>
		<h1> Machine Learning </h1>
		<p> Machine learning (ML) is a type of artificial intelligence (AI) that allows software applications to become more accurate at predicting outcomes without being explicitly programmed to do so. Machine learning algorithms use historical data as input to predict new output values.<p/>
		<h1> Mobile Computing </h1>
		<p> Mobile Computing is a technical field that covers the design, development and evaluation of mobile applications using appropriate solutions that meet user requirements. This includes learning the technology that is used to perform a wide variety of tasks on devices that are portable.<p/>
		<h1> Natural Language Processing </h1>
		<p> Natural language processing (NLP) refers to the branch of computer science—and more specifically, the branch of artificial intelligence or AI—concerned with giving computers the ability to understand text and spoken words in much the same way human beings can.<p/>
		<h1> Network Security </h1>
		<p> Network security is a broad term that covers a multitude of technologies, devices and processes. In its simplest term, it is a set of rules and configurations designed to protect the integrity, confidentiality and accessibility of computer networks and data using both software and hardware technologies. Every organization, regardless of size, industry or infrastructure, requires a degree of network security solutions in place to protect it from the ever-growing landscape of cyber threats in the wild today.<p/>
		<h1> Programming Languages </h1>
		<p> A programming language is any set of rules that converts strings, or graphical program elements in the case of visual programming languages, to various kinds of machine code output.[citation needed] Programming languages are one kind of computer language, and are used in computer programming to implement algorithms.<p/>
		<h1> Quantum Compunting </h1>
		<p>Quantum computing is an area of study focused on the development of computer based technologies centered around the principles of quantum theory. Quantum theory explains the nature and behavior of energy and matter on the quantum (atomic and subatomic) level. <p/>
		<h1> Robotics </h1>
		<p> Robotics is a branch of engineering that involves the conception, design, manufacture and operation of robots. The objective of the robotics field is to create intelligent machines that can assist humans in a variety of ways. Robotics can take on a number of forms.<p/>
		<h1> Software Engineering </h1>
		<p> Software engineering is the application of principles used in the field of engineering, which usually deals with physical systems, to the design, development, testing, deployment and management of software systems.<p/>
		<h1> UI / UX </h1>
		<p> UI refers to the screens, buttons, toggles, icons, and other visual elements that you interact with when using a website, app, or other electronic device. UX refers to the entire interaction you have with a product, including how you feel about the interaction.<p/>
		<h1> Wed Design </h1>
		<p> Web design refers to the design of websites that are displayed on the internet. It usually refers to the user experience aspects of website development rather than software development.<p/>
		<h1> Web Development </h1>
		<p> Web development, also known as website development, refers to the tasks associated with creating, building, and maintaining websites and web applications that run online on a browser. It may, however, also include web design, web programming, and database management.</p>
